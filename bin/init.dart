/// `dart run yaml_lint:init` — bootstraps yaml_lint in a project.
///
/// Design choice (CREATE-ONLY): this command **never** modifies an
/// existing file. If `analysis_options.yaml` or `lint_rules.yaml` is
/// already present, we print clear copy-paste instructions and exit
/// with a non-zero status. The "patch your YAML for me" path is
/// intentionally out of scope: hand-tuned `analysis_options.yaml`
/// files are common, and silent edits are a footgun.
///
/// Usage:
///
/// ```sh
/// dart run yaml_lint:init                  # in project root
/// dart run yaml_lint:init --root path/...  # custom project root
/// dart run yaml_lint:init --force          # overwrite existing files
/// ```
library;

import 'dart:io';

import 'package:path/path.dart' as p;

const _starterLintRulesYaml = '''
# yaml_lint rule set. See https://pub.dev/packages/yaml_lint for the full
# DSL reference. Uncomment / tweak rules as you adopt them.
version: 1

# Optional: declare your architectural layers so `import_from_layer:`
# rules can refer to them by name.
#
# layers:
#   domain:
#     paths: ["lib/domain/**"]
#   data:
#     paths: ["lib/data/**"]

rules:
  # 1. Forbid dart:io in pure-Dart code (uncomment to enable).
  # - id: no_dart_io
  #   target:
  #     type: import
  #     names: ["dart:io"]
  #   report:
  #     severity: error
  #     message: "dart:io is forbidden here"

  # 2. Require Analytics.track() in onTap / onPressed callbacks.
  # - id: include_analytics_tracking
  #   target:
  #     type: named_argument
  #     names: [onTap, onPressed]
  #   must_contain:
  #     method_call: [Analytics.track]
  #   report:
  #     severity: warning
  #     message: "Wire analytics into this callback"

  # 3. Keep print() out of build scripts.
  # - id: no_print_outside_lib
  #   target:
  #     type: method_call
  #     names: [print]
  #   scope:
  #     include: ["bin/**", "tool/**"]
  #   report:
  #     severity: warning
  #     message: "print() is forbidden outside lib/"
''';

const _starterAnalysisOptions = '''
# Generated by `dart run yaml_lint:init`. yaml_lint runs as a
# first-class analysis_server_plugin under the top-level `plugins:`
# section. The IDE and `dart analyze` will both honour these rules.
plugins:
  yaml_lint:
    # When using a published version on pub.dev:
    version: ^0.1.0
    # During local development, point at a checkout instead:
    # path: ../yaml_lint
''';

void main(List<String> args) {
  exitCode = _run(args, stdout: stdout, stderr: stderr);
}

int _run(
  List<String> args, {
  required IOSink stdout,
  required IOSink stderr,
}) {
  String? rootArg;
  var force = false;
  for (var i = 0; i < args.length; i++) {
    final a = args[i];
    switch (a) {
      case '--force':
      case '-f':
        force = true;
      case '--help':
      case '-h':
        _printHelp(stdout);
        return 0;
      case '--root':
        if (i + 1 >= args.length) {
          stderr.writeln('--root requires a value.');
          return 64; // EX_USAGE
        }
        rootArg = args[++i];
      default:
        if (a.startsWith('--root=')) {
          rootArg = a.substring('--root='.length);
        } else {
          stderr.writeln("Unknown argument '$a'. Run with --help.");
          return 64;
        }
    }
  }

  final root = rootArg == null
      ? Directory.current.path
      : p.absolute(rootArg);
  if (!Directory(root).existsSync()) {
    stderr.writeln("Root directory '$root' does not exist.");
    return 66; // EX_NOINPUT
  }
  if (!File(p.join(root, 'pubspec.yaml')).existsSync()) {
    stderr.writeln(
      "No pubspec.yaml in '$root'. Point --root at a Dart/Flutter package.",
    );
    return 66;
  }

  final lintRulesPath = p.join(root, 'lint_rules.yaml');
  final analysisOptionsPath = p.join(root, 'analysis_options.yaml');

  final created = <String>[];
  final skipped = <String>[];

  if (!_writeOnce(
    path: lintRulesPath,
    contents: _starterLintRulesYaml,
    force: force,
    created: created,
    skipped: skipped,
    stderr: stderr,
  )) {
    return 73; // EX_CANTCREAT
  }

  if (File(analysisOptionsPath).existsSync() && !force) {
    skipped.add(analysisOptionsPath);
    stdout.writeln(
      'analysis_options.yaml already exists; not modifying it. '
      'Add this snippet under your top-level `plugins:` block:\n'
      '\n'
      '  yaml_lint:\n'
      '    version: ^0.1.0',
    );
  } else if (!_writeOnce(
    path: analysisOptionsPath,
    contents: _starterAnalysisOptions,
    force: force,
    created: created,
    skipped: skipped,
    stderr: stderr,
  )) {
    return 73;
  }

  stdout.writeln('');
  stdout.writeln('yaml_lint initialised.');
  for (final c in created) {
    stdout.writeln('  + ${p.relative(c, from: root)} (created)');
  }
  for (final s in skipped) {
    stdout.writeln('  · ${p.relative(s, from: root)} (kept; use --force to overwrite)');
  }
  if (created.isNotEmpty) {
    stdout.writeln('');
    stdout.writeln(
      'Next: edit lint_rules.yaml to enable the rules you want, then '
      'run `dart analyze`.',
    );
  }
  return 0;
}

bool _writeOnce({
  required String path,
  required String contents,
  required bool force,
  required List<String> created,
  required List<String> skipped,
  required IOSink stderr,
}) {
  final f = File(path);
  if (f.existsSync() && !force) {
    skipped.add(path);
    return true;
  }
  try {
    f.writeAsStringSync(contents);
    created.add(path);
    return true;
  } on FileSystemException catch (e) {
    stderr.writeln("Could not write '$path': ${e.message}");
    return false;
  }
}

void _printHelp(IOSink stdout) {
  stdout.writeln('''
Usage: dart run yaml_lint:init [options]

Bootstraps yaml_lint in the current Dart/Flutter package by creating
`lint_rules.yaml` (and `analysis_options.yaml` if missing).

Options:
  --root <dir>   Project root to bootstrap (defaults to current directory).
  --force, -f    Overwrite files that already exist.
  --help, -h     Print this help message.
''');
}
